def get_chapter_voice_block(style_type: str | StyleType | None) -> str:
    key = style_type.value if isinstance(style_type, StyleType) else (style_type or "")
    try:
        st = StyleType(key)
    except ValueError:
        st = StyleType.popular_science
    return _CHAPTER_VOICE.get(st, _CHAPTER_VOICE[StyleType.popular_science])
